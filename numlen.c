#include "ft_printf.h"

int	numlen(unsigned int nbr)
{
	int	total;

	total = 0;
	if (nbr == 0)
		return (1);
	while (nbr > 0)
	{
		total += 1;
		nbr /= 10;
	}
	return (total);
}
